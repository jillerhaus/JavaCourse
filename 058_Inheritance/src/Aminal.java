public class Aminal {

    private String name;
    private int brain;
    private int body;
    private int size;
    private int weight;

    public Aminal() {
    }

    public Aminal(String name, int brain, int body, int size, int weight) {
        this.name = name;
        this.brain = brain;
        this.body = body;
        this.size = size;
        this.weight = weight;
    }
}
